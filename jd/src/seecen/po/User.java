package seecen.po;

public class User
{
	int uid;
	String uname;
	String upwd;
	String uemail;
	
	public int getUid()
	{
		return uid;
	}
	public void setUid(int uid)
	{
		this.uid = uid;
	}
	public String getUname()
	{
		return uname;
	}
	public void setUname(String uname)
	{
		this.uname = uname;
	}
	public String getUpwd()
	{
		return upwd;
	}
	public void setUpwd(String upwd)
	{
		this.upwd = upwd;
	}
	public String getUemail()
	{
		return uemail;
	}
	public void setUemail(String uemail)
	{
		this.uemail = uemail;
	}
}
